    private void jButtonExReceitaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonExReceitaActionPerformed
        int  resposta = 0;
        conex.conexao();
        int gastoLinha= jTableReceita.getSelectedRow();
        int gastoColuna = 0;
        int pegatabela = Integer.parseInt(jTableReceita.getValueAt(gastoLinha, gastoColuna).toString());
        resposta = JOptionPane.showConfirmDialog(rootPane, "Excluir receita?");
        if (resposta == JOptionPane.YES_OPTION){
            rec.setCodigo(pegatabela);
            control.Excluir(rec);
            TelaReceita tela = new TelaReceita();
            tela.setVisible(true);
            dispose();
        }
        conex.desconecta();
    }//GEN-LAST:event_jButtonExReceitaActionPerformed

