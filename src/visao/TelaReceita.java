    public void preencherTabela(String Sql){
        ArrayList dados = new ArrayList();
        String [] colunas = new String []{"Cod","Valor","Data"};
        conex.conexao();
        conex.executaSql(Sql);
        try{
            conex.rs.first();
            do{
                dados.add(new Object[]{conex.rs.getInt("cod_adicionarreceita"),conex.rs.getFloat("valor_adicionarreceita"),conex.rs.getDate("data_adicionarreceita")});
            }while(conex.rs.next());
        }catch(SQLException ex){

        }
        ModeloTabela modelo = new ModeloTabela(dados,colunas);
        
        jTableReceita.setModel(modelo);
        jTableReceita.getColumnModel().getColumn(0).setPreferredWidth(44);
        jTableReceita.getColumnModel().getColumn(0).setResizable(false);
        jTableReceita.getColumnModel().getColumn(1).setPreferredWidth(100);
        jTableReceita.getColumnModel().getColumn(1).setResizable(false);
        jTableReceita.getColumnModel().getColumn(2).setPreferredWidth(80);
        jTableReceita.getColumnModel().getColumn(2).setResizable(false);
        jTableReceita.getTableHeader().setReorderingAllowed(false);
        jTableReceita.setAutoResizeMode(jTableReceita.AUTO_RESIZE_OFF);
        jTableReceita.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);
        conex.desconecta();
    }
