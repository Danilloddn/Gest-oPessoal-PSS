    public void preencherTabela(String Sql){
        ArrayList dados = new ArrayList();
        String [] colunas = new String []{"Cod","Nome","Data"};
        conex.conexao();
        conex.executaSql(Sql);
        try{
            conex.rs.first();
            do{
                dados.add(new Object[]{conex.rs.getInt("cod_adicionardestino"),conex.rs.getString("nome_adicionardestino"),conex.rs.getDate("data_adicionardestino")});
            }while(conex.rs.next());
        }catch(SQLException ex){

        }
        ModeloTabela modelo = new ModeloTabela(dados,colunas);
        
        jTableDestino.setModel(modelo);
        jTableDestino.getColumnModel().getColumn(0).setPreferredWidth(44);
        jTableDestino.getColumnModel().getColumn(0).setResizable(false);
        jTableDestino.getColumnModel().getColumn(1).setPreferredWidth(100);
        jTableDestino.getColumnModel().getColumn(1).setResizable(false);
        jTableDestino.getColumnModel().getColumn(2).setPreferredWidth(80);
        jTableDestino.getColumnModel().getColumn(2).setResizable(false);
        jTableDestino.getTableHeader().setReorderingAllowed(false);
        jTableDestino.setAutoResizeMode(jTableDestino.AUTO_RESIZE_OFF);
        jTableDestino.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);
        conex.desconecta();
    }
    
