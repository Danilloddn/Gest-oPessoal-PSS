    private void jButtonExDestinoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonExDestinoActionPerformed
        int  resposta = 0;
        conex.conexao();
        int gastoLinha= jTableDestino.getSelectedRow();
        int gastoColuna = 0;
        int pegatabela = Integer.parseInt(jTableDestino.getValueAt(gastoLinha, gastoColuna).toString());
        resposta = JOptionPane.showConfirmDialog(rootPane, "Excluir destino?");
        if (resposta == JOptionPane.YES_OPTION){
            dest.setCodigo(pegatabela);
            control.Excluir(dest);
            TelaDestino tela = new TelaDestino();
            tela.setVisible(true);
            dispose();
        }
        conex.desconecta();
    }//GEN-LAST:event_jButtonExDestinoActionPerformed

